import base64


def adanient():
    file_path = "static/adanient.png"
    try:
        with open(file_path, "rb") as file:
            png_data = file.read()
            png_base64 = base64.b64encode(png_data).decode("utf-8")
        return png_base64
    except FileNotFoundError:
        print(f"File '{file_path}' not found.")
        return None


